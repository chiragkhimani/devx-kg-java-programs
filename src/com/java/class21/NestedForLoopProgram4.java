package com.java.class21;

// What will be the output of the program?

public class NestedForLoopProgram4 {
    public static void main(String[] args) {

        for (int i = 1; i <= 5; i++) {

            for (int j = 1; j <= 5; j++) {
                System.out.print(j);
            }

            System.out.println();
        }

    }
}
